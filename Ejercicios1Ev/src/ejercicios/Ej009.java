//Tabla de multiplicar de un numero
package ejercicios;

public class Ej009 {
    public static void main(String arg[]){
        int n = 5;
        //Multiplica el numero n por i, i va de 1 a 10
        for(int i = 1; i<=10; i++){
            System.out.println(n + " * " + i + " = " + (n*i));
        }
    }    
}
